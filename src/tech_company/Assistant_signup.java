    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // UPDATE:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/tech_company", "root", "");
            String query = "UPDATE `assistant` SET `aname`=?, `aaddress`=?, `qulification`=?, `number`=?  WHERE `aid`=?";
            PreparedStatement pst = con.prepareStatement(query);

            pst.setString(1, name1.getText());
            pst.setString(2, address1.getText());
            pst.setString(3, qulification1.getText());
            pst.setString(4, Number1.getText());
            pst.setString(5, c_search_tbl.getText());

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Details Updated");
            
            ADBTable();

            name1.setText("");
            address1.setText("");
            qulification1.setText("");
            Number1.setText("");
            c_search_tbl.setText("");
            c_search_tbl.requestFocus();

            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Delete:
    try {
        String aid = c_search_tbl.getText();

        Class.forName("com.mysql.jdbc.Driver");
        Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/tech_company", "root", "");
        PreparedStatement pst = con.prepareStatement("DELETE FROM assistant WHERE aid = ?");
        pst.setString(1, aid);

        int result = pst.executeUpdate();
        if (result > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Details deleted successfully");

            // Clear additional fields
            c_search_tbl.setText("");
            name1.setText("");
            address1.setText("");
            qulification1.setText("");
            Number1.setText("");
            c_search_tbl.requestFocus();

            // Reload your tables
            tb_load();
            ADBTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to delete details");
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, e);
    }

    }//GEN-LAST:event_jButton6ActionPerformed

