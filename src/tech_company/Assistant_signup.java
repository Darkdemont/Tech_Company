private void ADBTable() {
    DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
    tb.setRowCount(0); // Clear existing data in the table

    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/tech_company", "root", "");
        Statement st = con.createStatement();
        String q = "SELECT * FROM `assistant` WHERE 1;";
        ResultSet rs = st.executeQuery(q);

        while (rs.next()) {
            String aid = String.valueOf(rs.getInt(1));
            String aname = String.valueOf(rs.getString(2));
            String aaddress = String.valueOf(rs.getString(3));
            String qulification = String.valueOf(rs.getString(4));
            String number = String.valueOf(rs.getInt(5));
            String ausername = String.valueOf(rs.getString(6));
            String apassword = String.valueOf(rs.getString(7));

            String td[] = {aid, aname, aaddress, qulification, number, ausername, apassword};
            tb.addRow(td);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, e);
    }
}
    
